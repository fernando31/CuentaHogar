    private void btnSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirActionPerformed
        try {
            if (chkVolverMostrar.isSelected()) {
                ed.borrarFicheroIni();
            }
        } catch (Exception ex) {
            Logger.getLogger(VentanaInicial.class.getName()).log(Level.SEVERE, null, ex);
            ed.mostrarError("Fallo al gestionar el fichero \"cuentaHogar.ini\"", ex);
        }
        System.exit(0);
    }//GEN-LAST:event_btnSalirActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (!txtBBDD.getText().isEmpty() && !txtUser.getText().isEmpty() && txtPass.getPassword() != null) {
            HashMap<String, String> ficheroIni = new HashMap<String, String>();

            this.bbdd = txtBBDD.getText();
            this.user = txtUser.getText();
            this.pass = new String(txtPass.getPassword());

            if (this.comprobarConexion(bbdd, user, pass)) {

                try {
                    if (!chkVolverMostrar.isSelected()) {
                        ficheroIni = ed.crearFicheroIni(bbdd, user, pass, "no");
                    } else {
                        ed.borrarFicheroIni();
                    }
                } catch (Exception ex) {
                    Logger.getLogger(VentanaInicial.class.getName()).log(Level.SEVERE, null, ex);
                    ed.mostrarError("Fallo al gestionar el fichero \"cuentaHogar.ini\"", ex);
                }

                ElementosGraficos eg = new ElementosGraficos(bbdd, user, pass);
                eg.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Imposible conectar con la BBDD", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Existe campos vacios", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed
